    // The method that logs in the user, this method is used by the "login"
    // button in the login screen and the <enter> key also triggers this method
    private void loginMethod() {

        conn = JavaConnect.ConnecrDb();
        // login button logic is here
        String sqllogin = "select * from bagawaredb.ACCOUNT where username=?"
                + " and password=?";
        String resultSetAsString = null;
        locationOfLogin = (String) combobox_location.getSelectedItem();
        usernameOfLogin = txt_username.getText();
        try {

            pst = conn.prepareStatement(sqllogin);
            pst.setString(1, txt_username.getText());
            pst.setString(2, txt_password.getText());

            rs = pst.executeQuery();
            if (rs.next()) {

                String sqlAccountType = "select TYPE_typecode from"
                        + " bagawaredb.ACCOUNT where username=? and password=?";
                pst = conn.prepareStatement(sqlAccountType);
                pst.setString(1, txt_username.getText());
                pst.setString(2, txt_password.getText());
                rs = pst.executeQuery();

                //make resultset (rs) into string
                while (rs.next()) {

                    resultSetAsString = rs.getString("TYPE_typecode");

                }

                //check account type and invoke the corresponding window
                if (resultSetAsString.equals("admin")) {
                    //assign type for later use
                    typeOfLogin = "admin";
                    //close the login screen
                    this.dispose();
                    //open the admin main screen
                    final AdminMainScreen aMain = new AdminMainScreen();
                    aMain.setTitle("Administrator Main Screen");    // set title
                    aMain.setVisible(true);
                    java.awt.EventQueue.invokeLater(new Runnable() {
                        // these lines force the screen to the foreground
                        //and centere it
                        public void run() {
                            aMain.toFront();
                            aMain.repaint();
                            aMain.setLocationRelativeTo(null);
                        }
                    });

                } else if (resultSetAsString.equals("employee")) {
                    //assign type for later use
                    typeOfLogin = "employee";
                    //close the login screen
                    this.dispose();
                    //open the employee main screen
                    final EmployeeMainScreen eMain = new EmployeeMainScreen();
                    eMain.setTitle("Employee Main Screen");         // set title
                    eMain.setVisible(true);
                    java.awt.EventQueue.invokeLater(new Runnable() {
                        // these lines force the screen to the foreground 
                        //and centere it
                        public void run() {
                            eMain.toFront();
                            eMain.repaint();
                            eMain.setLocationRelativeTo(null);
                        }
                    });

                } else if (resultSetAsString.equals("manager")) {
                    //assign type for later use
                    typeOfLogin = "manager";
                    //close the login screen
                    this.dispose();
                    //open the manager main screen
                    final ManagerMainScreen mMain = new ManagerMainScreen();
                    mMain.setTitle("Manager Main Screen");
                    mMain.setVisible(true);
                    java.awt.EventQueue.invokeLater(new Runnable() {
                        // these lines force the screen to the foreground 
                        //and centere it
                        public void run() {
                            mMain.toFront();
                            mMain.repaint();
                            mMain.setLocationRelativeTo(null);
                        }
                    });

                } else {
                    //custom error in case a account in db has an unrecognised
                    //account type
                    JOptionPane.showMessageDialog(null, "Error, Type not found");
                }

            } else {
                //in case username and password are not found in database
                JOptionPane.showMessageDialog(null, "Username and password are"
                        + " incorrect");

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Connection to login server"
                    + " failed");
        }
        //----------------------------------------

        JavaConnect.closeDb();
    }

